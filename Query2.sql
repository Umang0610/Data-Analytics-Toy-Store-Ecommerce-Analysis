-- Order to Session Ratio or Conversion rate(Visitor-Customer)
create view conversion_rate as 
with cte_1 as(
	select count(distinct website_session_id) orders from cleaned_orders
),cte_2 as(
	select count(distinct website_session_id) sessions from cleaned_website_sessions
)select cte_1.orders,cte_2.sessions,(100.0 *cte_1.orders/cte_2.sessions)[Conversion Rate] from cte_1 cross join cte_2;
go
-- Number and Ratio of orders per session grouped by the source
with cte_3 as(
	select ws.utm_source[Source],count(distinct o.website_session_id) orders from cleaned_orders o join [dbo].[cleaned_website_sessions] ws on o.website_session_id=ws.website_session_id group by ws.utm_source
),cte_4 as(
	select ws.utm_source[Source],count(distinct ws.website_session_id) [Count of sessions] from cleaned_website_sessions ws group by ws.utm_source
)select cte_3.[Source],cte_3.orders,cte_4.[Count of Sessions],(100.0 *cte_3.orders/cte_4.[Count of sessions]) as[percentage by source] from cte_3 join cte_4 on cte_3.[Source]=cte_4.[Source] order by [percentage by source] desc
-- UTM Campaign wise Website sessions
select count(*),coalesce(utm_campaign,'direct') from [dbo].[cleaned_website_sessions] group by coalesce(utm_campaign,'direct')
select count(utm_campaign),coalesce(utm_campaign,'direct') from [dbo].[cleaned_website_sessions] group by coalesce(utm_campaign,'direct')
-- Total count of campaigns in website Sessions Table
select count(*),utm_campaign from [dbo].[cleaned_website_sessions] group by utm_campaign
-- Distinct UTM Campaigns
select distinct (utm_campaign) from [dbo].[cleaned_website_sessions]

--How many and what campaigns led to product item being bought?
select coalesce(ws.utm_campaign,'direct')[Campaign Name],count(*)[Marketing campaign that led to the order] from [dbo].[cleaned_website_sessions] ws join [dbo].[cleaned_orders] o on ws.website_session_id=o.website_session_id group by coalesce(ws.utm_campaign,'direct') order by [Marketing campaign that led to the order] desc

--Percentage success by campaign
with cte_5 as(
	select coalesce(utm_campaign,'direct')[Campaign type],count(*)[Count of marketing campaign] from [dbo].[cleaned_website_sessions] group by coalesce(utm_campaign,'direct')

),cte_6 as(
	select coalesce(ws.utm_campaign,'direct')[Campaign type],count(*)[Marketing campaign that led to the order] from [dbo].[cleaned_website_sessions] ws join [dbo].[cleaned_orders] o on ws.website_session_id=o.website_session_id group by coalesce(ws.utm_campaign,'direct') 
)select cte_6.[Campaign type],cte_5.[Count of marketing campaign],cte_6.[Marketing campaign that led to the order],(100.0*cte_6.[Marketing campaign that led to the order]/cte_5.[Count of marketing campaign])[Percentage of Marketing campaign success] from cte_5 join cte_6 on cte_5.[Campaign type]=cte_6.[Campaign type] order by [Percentage of Marketing campaign success] desc

---Revenue generated by each Campaign
with cte_7 as(
	select [website_session_id],coalesce([utm_campaign],'direct')[Campaign type] from [dbo].[cleaned_website_sessions]
),cte_8 as(
	select [website_session_id],[price_usd] from [dbo].[cleaned_orders]
)select cte_7.[Campaign type],sum(cte_8.[price_usd])[Revenue of the Campaign] from cte_7 join cte_8 on cte_7.website_session_id=cte_8.website_session_id group by cte_7.[Campaign type] order by [Revenue of the Campaign] desc;

