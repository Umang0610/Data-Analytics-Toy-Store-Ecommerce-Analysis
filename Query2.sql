-- Profit Margin by Product
select p.product_id,p.product_name,sum(o.profit)[Profit] from [dbo].[cleaned_orders] o left join [dbo].[cleaned_order_items] i on o.order_id=i.order_id left join [dbo].[cleaned_products] p on i.product_id=p.product_id group  by p.product_id,p.product_name order by [Profit] desc

select p.product_id,p.product_name,(100.0 * sum(o.profit)/sum(o.price_usd))[Profit Margin] from [dbo].[cleaned_order_items] o join [dbo].[cleaned_products] p on o.[product_id]=p.[product_id] group by p.product_name,p.product_id order by [Profit Margin] desc;

